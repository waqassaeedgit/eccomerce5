<script>
function deleteProductImage(productId) {
    if (confirm('Are you sure you want to delete this image?')) {
        const deleteBtn = document.getElementById('deleteImageBtn');
        deleteBtn.disabled = true;
        deleteBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i>';
        
        fetch(`/admin/products/${productId}/delete-image`, {
            method: 'DELETE',
            headers: {
                'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content,
                'Content-Type': 'application/json',
                'Accept': 'application/json'
            }
        })
        .then(response => response.json())
        .then(data => {
            if (data.success) {
                document.getElementById('productImage').src = '{{ asset("images/products/default.jpg") }}';
                document.getElementById('imageContainer').innerHTML = '<img src="{{ asset("images/products/default.jpg") }}" style="width: 200px; height: 200px; object-fit: cover; border: 2px solid #ddd;" class="rounded">';
                alert('Image deleted successfully!');
            } else {
                alert('Failed to delete image: ' + data.message);
                deleteBtn.disabled = false;
                deleteBtn.innerHTML = '<i class="fas fa-trash"></i>';
            }
        })
        .catch(error => {
            console.error('Error:', error);
            alert('An error occurred while deleting the image');
            deleteBtn.disabled = false;
            deleteBtn.innerHTML = '<i class="fas fa-trash"></i>';
        });
    }
}
</script>
